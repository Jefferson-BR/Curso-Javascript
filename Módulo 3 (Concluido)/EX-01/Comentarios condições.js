/*

- O que é uma Condição em JS ?

Uma condição em JavaScript é uma estrutura de controle que permite executar diferentes blocos de código com base em uma expressão booleana. 

A estrutura if-else é um exemplo de condição em JavaScript. Ela permite executar um bloco de código se uma determinada condição for verdadeira e outro bloco de código se a condição for falsa.

A condição é uma expressão que deve ser avaliada como verdadeira ou falsa. Se a condição for verdadeira, o bloco de código dentro do primeiro par de chaves será executado. Caso contrário, o bloco de código dentro do segundo par de chaves (opcional) será executado.

- Else if:

O "else if" é usado quando existem mais de duas possibilidades de resultados diferentes que podem ocorrer. Ele é uma extensão do "if-else" e pode ser encadeado várias vezes para adicionar quantas condições forem necessárias.

Se a condição anterior for falsa, a próxima condição será avaliada. Apenas o bloco de código correspondente à primeira condição verdadeira será executado. Caso nenhuma das condições seja verdadeira, o bloco de código dentro do "else" será executado.

O "else" no final é opcional e é executado somente se todas as condições anteriores forem falsas.
*/