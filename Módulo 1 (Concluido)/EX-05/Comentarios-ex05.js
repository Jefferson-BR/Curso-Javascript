/*

- Operadores Relacionais:

> (Maior que)
< (Menor que)
>= (Maior ou igual)
<= (Menor ou Igual)
== (Igual)
!= (Diferente de)

- Operador de identidade:

=== (Compara os valores para saber se são do mesmo tipo de dado, e o mesmo valor).

- Operadores Lógicos:

! (Negação)
&& (Conjunção)
|| (Disjunção)

Exemplos:

! Uma coisa que não é Verdadeira (true) é: -> Falso (false)
! Uma coisa que é Falsa (false) é: Verdadeira (true)

true && true = true
true && false = false
false && true = false
false && false = false

true || true = true
true || false = true
false || true = true
false || false = false

- Ordem de precedência:

() ** /
> < >=
!
&&
||

- Operador ternário:

(?:)

Teste ? true : false

*/